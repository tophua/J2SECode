package Mai123;

public class Person {
	 /** 姓名 **/
    private String name;
    
    /** 电子邮件 **/
    private String email;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
    
    

}
