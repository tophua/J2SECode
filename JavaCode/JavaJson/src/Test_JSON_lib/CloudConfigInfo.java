package Test_JSON_lib;

public class CloudConfigInfo {
	public String cloudstart;
	public String interval;
	public String cloudIP;
	public String cloudPort;
	public String omsIP;
	public String omsPort;
	public String getCloudstart() {
		return cloudstart;
	}
	public void setCloudstart(String cloudstart) {
		this.cloudstart = cloudstart;
	}
	public String getInterval() {
		return interval;
	}
	public void setInterval(String interval) {
		this.interval = interval;
	}
	public String getCloudIP() {
		return cloudIP;
	}
	public void setCloudIP(String cloudIP) {
		this.cloudIP = cloudIP;
	}
	public String getCloudPort() {
		return cloudPort;
	}
	public void setCloudPort(String cloudPort) {
		this.cloudPort = cloudPort;
	}
	public String getOmsIP() {
		return omsIP;
	}
	public void setOmsIP(String omsIP) {
		this.omsIP = omsIP;
	}
	public String getOmsPort() {
		return omsPort;
	}
	public void setOmsPort(String omsPort) {
		this.omsPort = omsPort;
	}
}
